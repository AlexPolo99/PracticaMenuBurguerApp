    private void jRadioButton_cerveza_polloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton_cerveza_polloActionPerformed
        grupo_pollo_bebida.add(jRadioButton_cerveza_pollo);
        if (jRadioButton_cerveza_pollo.isSelected()) {
            JOptionPane.showConfirmDialog(this, "¿Eres mayor de edad?", "Confirmacion de Mayoria de Edad", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (JOptionPane.YES_OPTION == 0) {
                jRadioButton_cerveza_pollo.setSelected(true);
            }
            if (JOptionPane.NO_OPTION == 1) {
                jRadioButton_cerveza_pollo.setSelected(false);
            }
            if (JOptionPane.CANCEL_OPTION == 2) {
                jRadioButton_cerveza_pollo.setSelected(false);
            }
            if (JOptionPane.CLOSED_OPTION == -1) {
                jRadioButton_cerveza_pollo.setSelected(false);
            }
        }
    }//GEN-LAST:event_jRadioButton_cerveza_polloActionPerformed

