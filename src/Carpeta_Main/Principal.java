    private void jButton_total_polloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_total_polloActionPerformed
        double precio = 8, ketchup, bbq, mostaza, thai, iva, total;
        if (jRadioButton_local_pollo.isSelected() == false && jRadioButton_domicilio_pollo.isSelected() == false) {
            JOptionPane.showMessageDialog(this, "Seleccione una opcion para continuar", "¡AVISO!", JOptionPane.WARNING_MESSAGE);
        }
        //Se calcula el precio de los costes adicionales
        if (jRadioButton_caseras_pollo.isSelected()) {
            precio = precio + 1;
        }
        if (jRadioButton_doble_pollo.isSelected()) {
            precio = precio + 2;
        }
        if (jRadioButton_queso_pollo.isSelected()) {
            precio = precio + 0.50;
        }
        if (jRadioButton_extra_patatas_pollo.isSelected()) {
            precio = precio + 1;
        }
        //Se calcula el precio total de cada salsa
        ketchup = 0.50 * jComboBox_ketchup_pollo.getSelectedIndex();
        bbq = 0.50 * jComboBox_bbq_pollo.getSelectedIndex();
        mostaza = 0.50 * jComboBox_mostaza_pollo.getSelectedIndex();
        thai = 0.50 * jComboBox_thai_pollo.getSelectedIndex();
        precio = precio + ketchup + bbq + mostaza + thai;
        if (jRadioButton_local_pollo.isSelected() == true || jRadioButton_domicilio_pollo.isSelected() == true) {
            num_ticket = num_ticket + 1;
            if (jRadioButton_local_pollo.isSelected()) {
                //Se calcula el descuento y el iva
                double descuento = precio * 0.2;
                precio = precio - descuento;
                iva = precio * 0.21;
                total = precio + iva;
                JOptionPane.showMessageDialog(this, "Numero de ticket: " + num_ticket + "\n" + "El precio del pedido sin I.V.A es: " + precio + "€" + "\n" + "EL I.V.A es: " + decimales.format(iva) + "€" + "\n" + "El precio total con I.V.A es: " + decimales.format(total) + "€", "Recibo", JOptionPane.INFORMATION_MESSAGE);
            }
            if (jRadioButton_domicilio_pollo.isSelected()) {
                //Se calcula el iva
                iva = precio * 0.21;
                total = precio + iva;
                JOptionPane.showMessageDialog(this, "Numero de ticket: " + num_ticket + "\n" + "El precio del pedido sin I.V.A es: " + precio + "€" + "\n" + "EL I.V.A es: " + decimales.format(iva) + "€" + "\n" + "El precio total con I.V.A es: " + decimales.format(total) + "€", "Recibo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton_total_polloActionPerformed

